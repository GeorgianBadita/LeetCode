from typing import List


def valid_coord(i, j, grid):
    return 0 <= i < len(grid) and 0 <= j < len(grid[0]) and grid[i][j] == "1"


class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        def fill(i, j):
            grid[i][j] = "-"

            if valid_coord(i + 1, j, grid):
                fill(i + 1, j)
            if valid_coord(i - 1, j, grid):
                fill(i - 1, j)
            if valid_coord(i, j + 1, grid):
                fill(i, j + 1)
            if valid_coord(i, j - 1, grid):
                fill(i, j - 1)

        num_islands = 0
        for i in range(len(grid)):
            for j in range(len(grid[0])):
                if grid[i][j] == "1":
                    num_islands += 1
                    fill(i, j)

        return num_islands


